import turtle

turtle.color("blue")
turtle.circle(100)

turtle.penup()
turtle.goto(200,0)
turtle.pendown()
turtle.color("green")
turtle.circle(100)


turtle.penup()
turtle.goto(-200,0)
turtle.pendown()
turtle.color("pink")
turtle.circle(100)

turtle.penup()
turtle.goto(-100,-100)
turtle.pendown()
turtle.color("yellow")
turtle.circle(100)

turtle.penup()
turtle.goto(100,-100)
turtle.pendown()
turtle.color("black")
turtle.circle(100)
turtle.pendown()

turtle.done()